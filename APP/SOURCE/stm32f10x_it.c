/*******************************************************************************
* Function Name  : SysTickHandler
* Description    : This function handles SysTick Handler.
* Input          : None
* Output         : None
* Return         : None
*******************************************************************************/
void SysTickHandler(void)
{
   /* Clock beat 0.1ms once, as the clock beat of ucos*/
   OS_CPU_SR  cpu_sr;
   OS_ENTER_CRITICAL();  //Save the global interrupt flag, off the total interrupt/* Tell uC/OS-II that we are starting an ISR*/
   OSIntNesting++;	   
   OS_EXIT_CRITICAL();	 //Restore global interrupt flag
   //Defined in the os_core.c file, mainly to determine whether the delayed task is timed to
   OSTimeTick();     //Call uC/OS-II's OSTimeTick()
   ADC_TIMEOUT++;
   if(ADC_TIMEOUT>10)//ADC acquire 1 time/1ms
    {
       ADC_TIMEOUT=0;
       ADC_R=1;			//Set the interface display with the ADC sampling completion flag
       OSSemPost(ADC_SEM);	//Transmit ADC sample semaphore
     } 
   OSIntExit();  //Defined in the os_core.c file, if a higher priority task is ready, perform a task switch
}


